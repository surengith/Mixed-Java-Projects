package encapsulation;

public class Demo_Encapsulation {

	
	
	public void name() {
		System.out.println("Surendhar");
	}
	
	public void Std() {
		System.out.println("Fifth");
	}
	
	public void sub() {
		System.out.println("Maths");
	}
	
	public void time() {
		System.out.println("12:30");
	}
	
	public void date() {
		System.out.println("12.6.2022");
	}
	
	
	public static void main(String[] args) {
	
		
		Demo_Encapsulation obj = new Demo_Encapsulation();
		
		obj.name();
		obj.Std();
		obj.sub();
		obj.time();
		obj.date();
		
		}
		

	}

